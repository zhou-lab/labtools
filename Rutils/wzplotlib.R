plotdens <- function(x, normalize=FALSE, add=FALSE, ...) {
  d <- density(na.omit(x))
  if (normalize) {
    d$y <- d$y / max(d$y)
  }
  if (add) {
    lines(d, ...)
  } else {
    plot(d, ...)
  }
}
